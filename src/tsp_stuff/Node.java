package tsp_stuff;

public class Node {
	private double x;
	private double y;
	private double n;
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public double getN() {
		return n;
	}
	public void setN(double n) {
		this.n = n;
	}
	
	
}
